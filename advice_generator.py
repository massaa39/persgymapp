def create_advice(age, beauty_goals, preferred_seniority, medical_history, training_goals, medical_conditions=None):
    age = int(age)
    # アドバイス生成のロジックを実装
    advice = ""
    if age >= 58:
        advice += "お客様向けの運動プランを提供し、安全な運動環境を整えます。"

    if medical_history == "yes":
        advice += "持病があるため、トレーニング前に医師と相談し、適切な運動プログラムを決定してください。"

    if beauty_goals == "skin-care":
        advice += "スキンケアに関する知識を深め、お肌の状態を改善するための効果的なトレーニングを提案します。""適度な運動は血行を良くし、お肌に潤いを与えることができます。また、運動後は保湿ケアを忘れずに行いましょう。"

    elif beauty_goals == "body-shape":
        advice += "ボディシェイプ向上を目指すトレーニングメニューや、美容に役立つ食事に関するアドバイスを提供します。""全身の筋力トレーニングと有酸素運動を組み合わせることで、理想のボディシェイプを目指しましょう。トレーニングの強度と種類を適切に組み合わせることが大切です。"

    elif beauty_goals == "weight-loss":
        advice += "ダイエットに効果的な食事プランや運動方法を提案し、健康的な体重減少をサポートします。""食事管理と適度な運動が効果的なダイエットに繋がります。軽い有酸素運動や筋トレを組み合わせ、適切なカロリー摂取量を意識して食事管理を行いましょう。"

    elif beauty_goals == "stress-reduction":
         advice += "ストレス軽減に役立つトレーニングやリラクセーション方法を提案し、心身のバランスを整えます。""ストレス軽減には、リラックス効果のある運動や深呼吸が効果的です。散歩や瞑想を取り入れて、心身をリフレッシュしましょう。"


    if training_goals == "weight-loss":
        advice += "効果的なダイエットプログラムを提案し、脂肪燃焼を促進する運動方法をお伝えします。""適度な有酸素運動と筋トレが効果的です。ウォーキングや軽いジョギングを取り入れることで、心肺機能の向上にもつながります。"

    elif training_goals == "muscle-building":
        advice += "筋力アップに役立つトレーニングメニューを提案し、筋力を効率的に向上させる方法をお伝えします。""筋力トレーニングは、筋肉量を維持・増加させるために重要です。適切な重さと反復回数で、全身の筋肉に対してバランスよくトレーニングを行いましょう。"

    elif training_goals == "endurance":
        advice += "持久力アップに効果的な運動プログラムを提案し、持久力を向上させるための方法をお伝えします。""持久力を向上させるには、ウォーキング、ジョギングなどの有酸素運動を取り入れましょう。週に3-4回、30分から1時間程度を目安に運動を行ってください。"

    elif training_goals == "flexibility":
        advice += "柔軟性を向上させるためには、ストレッチや筋膜リリースが効果的です。運動前後にストレッチを行い、筋肉の柔軟性を保ちましょう。""柔軟性アップに効果的なストレッチングやトレーニングメニューを提案し、柔軟性を向上させる方法をお伝えします。"


    if preferred_seniority == "morning":
        advice += "朝の運動は、新陳代謝を促進し、一日の活力を高める効果があります。運動後に栄養バランスの良い朝食を摂取しましょう。"

    elif preferred_seniority == "daytime":
        advice += "昼間の運動は、仕事や家事の合間にリフレッシュする効果があります。適度な休憩を取り入れながら効率的に運動を行いましょう。"

    elif preferred_seniority == "evening":
        advice += "夕方の運動は、1日のストレスを解消し、リラックス効果が期待できます。ただし、寝る前の運動は興奮状態を引き起こすことがあるため、就寝2時間前までに運動を終えるようにしましょう。"


    if isinstance(medical_conditions, list):
        for medical_condition in medical_conditions:

            if medical_condition == "糖尿病":
               advice += "運動前にブドウ糖を摂取し、血糖値が安定してから、低～中負荷の有酸素運動と筋力トレーニングを行いましょう。運動後も、ブドウ糖を摂取して血糖値を正常範囲に戻してください。"

            elif medical_condition == "高血圧":
                advice += "有酸素運動と筋力トレーニングの両方が重要です。等張性運動を中心に取り入れ、過度な負荷を避けながら運動しましょう。また、心拍数を抑えた運動を行うことで、血圧を下げる効果が期待できます。"

            elif medical_condition == "関節リウマチ":
                advice += "関節に負担をかけない等尺性運動や等張性運動が適切です。筋力トレーニングによって筋肉を強化し、関節への負担を軽減できます。ただし、痛みがある場合は無理をしないように注意しましょう。"
 
            elif medical_condition == "人工関節または大腿骨の骨切り術後":
                advice += "股関節の内転や内旋方向への動きは避け、軽い有酸素運動や筋力トレーニングが適切ですが、運動前に必ず医師と相談してください。"

            elif medical_condition == "腱板縫合手術歴がある":
                advice += "肩関節外転90度以下の運動は避け、軽い有酸素運動や筋力トレーニングを中心に行いましょう。運動前に医師と相談して、適切な運動方法を選択してください。"

            elif medical_condition == "企図振戦":
                advice += "筋力トレーニングが有効ですが、適切な負荷やトレーニング方法を医師と相談して決めてください。"

            elif medical_condition == "腰痛持ちの方":
                advice += "軽い有酸素運動やストレッチから始め、負荷を徐々に増やしていくことが重要です。背骨筋力トレーニングによって腰周りの筋肉を強化し、腰痛の予防に役立ちます。運動中に腰に痛みが生じた場合は、すぐに運動を中止し、医師に相談してください。運動前にも医師の指導を仰ぎ、適切な運動方法を選択してください。"

            elif medical_condition == "慢性呼吸不全":
                advice += "運動前にSpO2を測定し、低い場合は軽度に運動するようにしましょう。"

            elif medical_condition == "歩行速度が低下している":
                advice += "歩幅を早く、早歩きを意識して習慣化させることが大切です。"
   
            elif medical_condition == "多剤を服用している":
                advice += "ふらつきや転倒に注意しましょう。"

            elif medical_condition == "高齢者":
                advice += "話は十分に時間をかけて聞くことが必要で、不眠が認められる割合が高いため、適切な対策が必要です。"

            elif medical_condition == "運動不足":
                advice += "適度な運動プログラムを設定し、継続しやすいようにサポートする工夫が必要です。"

            else:
                advice += "運動前に十分なウォーミングアップを行い、急激な運動開始は避けましょう。また、運動後はクールダウンとストレッチを行い、筋肉の緊張を緩和させましょう。運動中に体調が悪くなった場合や異常を感じた場合は、すぐに運動を中止し、医師に相談してください。定期的に健康チェックを受けることも大切です。"

    return advice

